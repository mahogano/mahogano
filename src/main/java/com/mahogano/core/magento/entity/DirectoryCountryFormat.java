package com.mahogano.core.magento.entity;

public class DirectoryCountryFormat {

    private Integer countryFormatId;

    private String countryId;

    private String type;

    private String format;

    public Integer getCountryFormatId() {
        return countryFormatId;
    }

    public void setCountryFormatId(Integer countryFormatId) {
        this.countryFormatId = countryFormatId;
    }

    public String getCountryId() {
        return countryId;
    }

    public void setCountryId(String countryId) {
        this.countryId = countryId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }

}

