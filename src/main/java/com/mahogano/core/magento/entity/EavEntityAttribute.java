package com.mahogano.core.magento.entity;

public class EavEntityAttribute {

    private Integer entityAttributeId;

    private Integer entityTypeId;

    private Integer attributeSetId;

    private Integer attributeGroupId;

    private Integer attributeId;

    private Integer sortOrder;

    public Integer getEntityAttributeId() {
        return entityAttributeId;
    }

    public void setEntityAttributeId(Integer entityAttributeId) {
        this.entityAttributeId = entityAttributeId;
    }

    public Integer getEntityTypeId() {
        return entityTypeId;
    }

    public void setEntityTypeId(Integer entityTypeId) {
        this.entityTypeId = entityTypeId;
    }

    public Integer getAttributeSetId() {
        return attributeSetId;
    }

    public void setAttributeSetId(Integer attributeSetId) {
        this.attributeSetId = attributeSetId;
    }

    public Integer getAttributeGroupId() {
        return attributeGroupId;
    }

    public void setAttributeGroupId(Integer attributeGroupId) {
        this.attributeGroupId = attributeGroupId;
    }

    public Integer getAttributeId() {
        return attributeId;
    }

    public void setAttributeId(Integer attributeId) {
        this.attributeId = attributeId;
    }

    public Integer getSortOrder() {
        return sortOrder;
    }

    public void setSortOrder(Integer sortOrder) {
        this.sortOrder = sortOrder;
    }

}

