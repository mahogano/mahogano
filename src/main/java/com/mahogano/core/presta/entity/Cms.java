package com.mahogano.core.presta.entity;


public class Cms {

    private Integer idCms;

    private Integer idCmsCategory;

    private Integer position;

    private Integer active;

    private Integer indexation;

    public Integer getIdCms() {
        return idCms;
    }

    public void setIdCms(Integer idCms) {
        this.idCms = idCms;
    }

    public Integer getIdCmsCategory() {
        return idCmsCategory;
    }

    public void setIdCmsCategory(Integer idCmsCategory) {
        this.idCmsCategory = idCmsCategory;
    }

    public Integer getPosition() {
        return position;
    }

    public void setPosition(Integer position) {
        this.position = position;
    }

    public Integer getActive() {
        return active;
    }

    public void setActive(Integer active) {
        this.active = active;
    }

    public Integer getIndexation() {
        return indexation;
    }

    public void setIndexation(Integer indexation) {
        this.indexation = indexation;
    }

}

