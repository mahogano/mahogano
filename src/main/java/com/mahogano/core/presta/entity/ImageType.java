package com.mahogano.core.presta.entity;


public class ImageType {

    private Integer idImageType;

    private String name;

    private Integer width;

    private Integer height;

    private Integer products;

    private Integer categories;

    private Integer manufacturers;

    private Integer suppliers;

    private Integer stores;

    public Integer getIdImageType() {
        return idImageType;
    }

    public void setIdImageType(Integer idImageType) {
        this.idImageType = idImageType;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getProducts() {
        return products;
    }

    public void setProducts(Integer products) {
        this.products = products;
    }

    public Integer getCategories() {
        return categories;
    }

    public void setCategories(Integer categories) {
        this.categories = categories;
    }

    public Integer getManufacturers() {
        return manufacturers;
    }

    public void setManufacturers(Integer manufacturers) {
        this.manufacturers = manufacturers;
    }

    public Integer getSuppliers() {
        return suppliers;
    }

    public void setSuppliers(Integer suppliers) {
        this.suppliers = suppliers;
    }

    public Integer getStores() {
        return stores;
    }

    public void setStores(Integer stores) {
        this.stores = stores;
    }

}

