package com.mahogano.core.presta.entity;


public class MetaLang {

    private Integer idMeta;

    private Integer idShop;

    private Integer idLang;

    private String title;

    private String description;

    private String keywords;

    private String urlRewrite;

    public Integer getIdMeta() {
        return idMeta;
    }

    public void setIdMeta(Integer idMeta) {
        this.idMeta = idMeta;
    }

    public Integer getIdShop() {
        return idShop;
    }

    public void setIdShop(Integer idShop) {
        this.idShop = idShop;
    }

    public Integer getIdLang() {
        return idLang;
    }

    public void setIdLang(Integer idLang) {
        this.idLang = idLang;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getKeywords() {
        return keywords;
    }

    public void setKeywords(String keywords) {
        this.keywords = keywords;
    }

    public String getUrlRewrite() {
        return urlRewrite;
    }

    public void setUrlRewrite(String urlRewrite) {
        this.urlRewrite = urlRewrite;
    }

}

