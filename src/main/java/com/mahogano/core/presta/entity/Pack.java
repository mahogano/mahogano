package com.mahogano.core.presta.entity;


public class Pack {

    private Integer idProductPack;

    private Integer idProductItem;

    private Integer idProductAttributeItem;

    private Integer quantity;

    public Integer getIdProductPack() {
        return idProductPack;
    }

    public void setIdProductPack(Integer idProductPack) {
        this.idProductPack = idProductPack;
    }

    public Integer getIdProductItem() {
        return idProductItem;
    }

    public void setIdProductItem(Integer idProductItem) {
        this.idProductItem = idProductItem;
    }

    public Integer getIdProductAttributeItem() {
        return idProductAttributeItem;
    }

    public void setIdProductAttributeItem(Integer idProductAttributeItem) {
        this.idProductAttributeItem = idProductAttributeItem;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

}

