package com.mahogano.core.presta.entity;


public class Page {

    private Integer idPage;

    private Integer idPageType;

    private Integer idObject;

    public Integer getIdPage() {
        return idPage;
    }

    public void setIdPage(Integer idPage) {
        this.idPage = idPage;
    }

    public Integer getIdPageType() {
        return idPageType;
    }

    public void setIdPageType(Integer idPageType) {
        this.idPageType = idPageType;
    }

    public Integer getIdObject() {
        return idObject;
    }

    public void setIdObject(Integer idObject) {
        this.idObject = idObject;
    }

}

