package com.mahogano.core.presta.entity;


public class ProductCarrier {

    private Integer idProduct;

    private Integer idCarrierReference;

    private Integer idShop;

    public Integer getIdProduct() {
        return idProduct;
    }

    public void setIdProduct(Integer idProduct) {
        this.idProduct = idProduct;
    }

    public Integer getIdCarrierReference() {
        return idCarrierReference;
    }

    public void setIdCarrierReference(Integer idCarrierReference) {
        this.idCarrierReference = idCarrierReference;
    }

    public Integer getIdShop() {
        return idShop;
    }

    public void setIdShop(Integer idShop) {
        this.idShop = idShop;
    }

}

