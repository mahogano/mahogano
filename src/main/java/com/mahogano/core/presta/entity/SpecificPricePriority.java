package com.mahogano.core.presta.entity;


public class SpecificPricePriority {

    private Integer idSpecificPricePriority;

    private Integer idProduct;

    private String priority;

    public Integer getIdSpecificPricePriority() {
        return idSpecificPricePriority;
    }

    public void setIdSpecificPricePriority(Integer idSpecificPricePriority) {
        this.idSpecificPricePriority = idSpecificPricePriority;
    }

    public Integer getIdProduct() {
        return idProduct;
    }

    public void setIdProduct(Integer idProduct) {
        this.idProduct = idProduct;
    }

    public String getPriority() {
        return priority;
    }

    public void setPriority(String priority) {
        this.priority = priority;
    }

}

